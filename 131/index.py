# The join() and split() Methods

# the join() method is useful when you have a list of strs that need to be joined together into a single str value. 

print(', '.join(['cats', 'rats', 'bats']))

print(' '.join(['My', 'name', 'is', 'Simon']))

print('ABC'.join(['My', 'name', 'is', 'Simon']))

# the str join() calls on is inserted between each str of the list arg.