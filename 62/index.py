# Augmented Assignment Operators

# When assigning a value to a var, you will frequently use the var itself. For example, after assigning 42 to the var spam, you would increase the value in spam by 1 with the following code:

spam = 42
spam = spam + 1
print(spam)

# As a shortcut, you can use the augmented assignment operator += to do the same thing:
spam = 42
spam += 1
print(spam)

# Augmented assignment statement | Equivalent assignment statement:

# spam += 1 | spam = spam + 1
# spam -= 1 | spam = spam - 1
# spam *= 1 | spam = spam * 1
# spam /= 1 | spam = spam / 1
# spam %= 1 | spam = spam % 1