# Negative Indexes

# The int value -1 refers to the last index in a list.
# The value -2 refers to the second-to-last index in a list, and so on

spam = ['cat', 'bat', 'rat', 'elephant']

print(spam[-1])
print(spam[-3])
print('The ' + spam[-1] + ' is afraid of the ' + spam[-3] + '.')
print(f"The {spam[-1]} is afraid of the {spam[-3]}.")