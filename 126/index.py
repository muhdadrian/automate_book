# the isupper() and islower() methods will return a Boolean True value if the str has at least one letter and all the letters are uppercase or lowercase, respectively. 

# enter the following into the interactive shell

spam = 'Hello, world!'
print(spam.islower())
print(spam.isupper())
print('HELLO'.isupper())
print('abc12345'.islower())
print('12345'.islower())
print('12345'.isupper())