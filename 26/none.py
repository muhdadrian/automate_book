# The None Value

# None value is the only value of the NoneType data type.
# == null, nil, or undefined
# None must be typed with capital letter of N
# None is used as the return value of print()
# print() function displays text on the screen, but it doesn't need to return anything like len()
# or input() does.

spam = print('Hello!')
print(None == spam)

