# Removing Whitespace with the strip(), rstrip(), and lstrip() Methods

# strip() str method -- return a new str without any whitespace chars at the beginning or end.

# lstrip() and rstrip() methods -- remove whitepspace chars from the left and right ends, respectively.

# enter the following into the interactive shell:

spam = '    Hello, World    '
print(spam.strip())
print(spam.lstrip())
print(spam.rstrip())

# the explanation is clearer in interactive shell