total = 0
for num in range(101):
    total = total + num
print(total)

# Initially, total is 0.
# In the first iteration, num is 0. total becomes 0 + 0 = 0.
# In the second iteration, num is 1. total becomes 0 + 1 = 1.
# In the third iteration, num is 2. total becomes 1 + 2 = 3.
# This process continues until num is 100.
